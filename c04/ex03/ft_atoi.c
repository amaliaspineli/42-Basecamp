int	ft_atoi(char *str)
{
	int i;
	int o;
	int r;

	i = 0;
	o = 1;
	r = 0;
	while (str[i] == '\f' || str[i] == '\n' || str[i] == '\r'
		|| str[i] == '\t' || str[i] == '\v' || str[i] == ' ')
		i++;
	while (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			o *= -1;
		i++;
	}
	while (str[i] > 47 && str[i] < 58)
	{
		r = (r * 10) + (str[i] - 48);
		i++;
	}
	return (o * r);
}
